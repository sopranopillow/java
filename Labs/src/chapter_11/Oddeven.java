package chapter_11;

public class Oddeven {
	String wrd;
	
	public Oddeven(String wr)
	{
		wrd = wr;
	}
	
	public void getA()
	{
		if(wrd.length()%2 == 1) System.out.println(wrd + " is odd");
		else System.out.println(wrd + " is even");
	}
	
	public static void main(String args[])
	{
		Oddeven o = new Oddeven("cat");
		o.getA();
	}
}
